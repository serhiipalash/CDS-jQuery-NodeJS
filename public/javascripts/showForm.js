function showForm(customer) {
  var customer = customer || {};
  var formContent = '';

  formContent = [
    '<form class="form-group" id="customerForm">',
      '<h1 id="formTitle" class="h1 text-center">',
        customer.name ? parseOb(customer.name, ' ') : 'New Customer',
      '</h1>',
      '<div class="row">',
        '<div class="form-group col-sm-6">',
          '<label for="firstName" class="control-label hidden-xs">First Name</label>',
          '<input type="text" id="firstName" class="form-control" value="',
            customer.name ? customer.name.firstName : '',
          '" required placeholder="First Name">',
        '</div>',
        '<div class="form-group col-sm-6">',
          '<label for="lastName" class="control-label hidden-xs">Last Name</label>',
          '<input type="text" id="lastName" class="form-control" value="',
            customer.name ? customer.name.lastName : '',
          '" placeholder="Last Name">',
        '</div>',
        '</div>',
        '<div class="row">',
          '<div class="form-group col-sm-6">',
            '<label for="email" class="control-label hidden-xs">Email</label>',
            '<input type="text" id="email" class="form-control" value="',
              customer.email ? customer.email : '',
            '" placeholder="Email">',
          '</div>',
          '<div class="form-group col-sm-6">',
            '<label for="telephone" class="control-label hidden-xs">Telephone</label>',
            '<input type="text" id="telephone" class="form-control" value="',
              customer.telephone ? customer.telephone : '',
            '" placeholder="Telephone">',
          '</div>',
        '</div>',
        '<div class="row">',
          '<div class="form-group col-sm-12">',
            '<label for="street" class="control-label hidden-xs">Street</label>',
            '<input type="text" id="street" class="form-control" value="',
              customer.address ? customer.address.street : '',
            '" placeholder="Street">',
          '</div>',
        '</div>',
        '<div class="row">',
          '<div class="form-group col-sm-4">',
            '<label for="city" class="control-label hidden-xs">City</label>',
            '<input type="text" id="city" class="form-control" value="',
              customer.address ? customer.address.city : '',
            '" placeholder="City">',
          '</div>',
          '<div class="form-group col-sm-4">',
            '<label for="state" class="control-label hidden-xs">State</label>',
            '<input type="text" id="state" class="form-control" value="',
              customer.address ? customer.address.state : '',
            '" placeholder="State">',
          '</div>',
          '<div class="form-group col-sm-4">',
            '<label for="zip" class="control-label hidden-xs">ZIP</label>',
            '<input type="text" id="zip" class="form-control" value="',
              customer.address ? customer.address.zip : '',
            '" placeholder="ZIP">',
          '</div>',
        '</div>',
        '<button id="submit" type="submit" class="btn btn-primary btn-block">OK</button>',
      '</form>'
  ].join('');

  $('#content').append(formContent);
  $('#firstName').focus();
}
